package com.jieandata.service.model.plan;

import lombok.Data;

/**
 * @Auther: pengke
 * @Date: 2019-8-7 17:19
 * @Description: 计划详细信息
 */
@Data
public class PlanDetailInfoVO {
    private String orderNo;
    private String planType;
    private String money;
    private String userRateFee;
    private String gkRateFee;
    private String aisleRateFee;
    private String planDate;
    private String planStatus;
    private String logString;

    public PlanDetailInfoVO(String orderNo, String planType,
                            String money, String userRateFee,
                            String gkRateFee, String aisleRateFee,
                            String planDate, String planStatus,
                            String logString) {
        this.orderNo = orderNo;
        this.planType = planType;
        this.money = money;
        this.userRateFee = userRateFee;
        this.gkRateFee = gkRateFee;
        this.aisleRateFee = aisleRateFee;
        this.planDate = planDate;
        this.planStatus = planStatus;
        this.logString = logString;
    }
}
